package chapter2;

public class Order {

	String orderNum;
	String id;
	String orderDate;
	String name;
	String productNum;
	String address;
	
	public String getOrderNum() {
		return orderNum;
	}
	
	public String getId() {
		return id;
	}
	
	public String getOrderDate() {
		return orderDate;
	}
	
	public String getName() {
		return name;
	}
	
	public String getProductNum() {
		return productNum;
	}
	
	public String getAddress() {
		return address;
	}
}
